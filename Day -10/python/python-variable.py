# Assigning values to variables
x = 5       # Integer
y = 3.14    # Floating point number
name = "Alice"  # String
is_active = True  # Boolean

# Printing variables
print(x)          # Output: 5
print(y)          # Output: 3.14
print(name)       # Output: Alice
print(is_active)  # Output: True

# Changing the value of a variable
x = 10
print(x)          # Output: 10
